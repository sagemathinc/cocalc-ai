#!/usr/bin/env node

const { createHash } = require("node:crypto");
const {
  readdirSync,
  readFileSync,
  statSync,
  writeFileSync,
} = require("node:fs");
const { join, relative, resolve } = require("node:path");

const packageRoot = resolve(__dirname, "..");
const repoRoot = resolve(packageRoot, "../..");
const recipeRoot = join(repoRoot, "packages", "rootfs-recipes");
const out = join(
  packageRoot,
  "src",
  "bin",
  "commands",
  "rootfs-recipes-builtin.generated.ts",
);

const files = {};

function walk(dir) {
  for (const name of readdirSync(dir)) {
    const path = join(dir, name);
    const stat = statSync(path);
    if (stat.isDirectory()) {
      walk(path);
    } else if (stat.isFile()) {
      const rel = relative(recipeRoot, path).split(/[\\/]/).join("/");
      files[rel] = readFileSync(path, "utf8");
    }
  }
}

walk(recipeRoot);

const hash = createHash("sha256");
for (const key of Object.keys(files).sort()) {
  hash.update(key);
  hash.update("\0");
  hash.update(files[key]);
  hash.update("\0");
}

writeFileSync(
  out,
  `/*\n * Generated by packages/cli/scripts/generate-rootfs-recipes.js.\n * Do not edit by hand.\n */\n\nexport const BUILTIN_ROOTFS_RECIPES: {\n  version: 1;\n  hash: string;\n  files: Record<string, string>;\n} = ${JSON.stringify(
    {
      version: 1,
      hash: hash.digest("hex"),
      files,
    },
    null,
    2,
  )};\n`,
);
